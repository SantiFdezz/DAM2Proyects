package com.example.airpeek.ui.GoTo;

public class GoToData {
    private String origin;
    private String destination;
    private int adults;
    private int kids;
    private int babies;
    private String goToDate;
    private String returnDate;
    public String getOrigin() { return origin; }

    public void setOrigin(String origin) { this.origin = origin; }

    public String getDestination() { return destination; }

    public void setDestination(String destination) { this.destination = destination; }

    public int getAdults() { return adults; }

    public void setAdults(int adults) { this.adults = adults; }

    public int getKids() { return kids; }

    public void setKids(int kids) { this.kids = kids; }

    public int getBabies() { return babies; }

    public void setBabies(int babies) { this.babies = babies; }

    public String getGoToDate() { return goToDate; }

    public void setGoToDate(String goToDate) { this.goToDate = goToDate; }

    public String getReturnDate() {
        return returnDate;
    }

    public void setReturnDate(String returnDate) {
        this.returnDate = returnDate;
    }

}
